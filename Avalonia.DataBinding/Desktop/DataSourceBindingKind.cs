namespace Avalonia.DataBinding.Desktop;

/// <summary>
/// Identifies the kind of a DataSource binding.
/// </summary>
public enum DataSourceBindingKind
{
    /// <summary>
    /// Represents a control binding.
    /// </summary>
    Control,
    /// <summary>
    /// Represents a grid binding.
    /// </summary>
    Grid,
    /// <summary>
    /// Represents a grid column binding.
    /// </summary>
    GridColumn
}
